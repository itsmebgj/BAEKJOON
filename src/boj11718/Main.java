package src.boj11718;

import java.util.Scanner;
public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        
        while(scanner.hasNext()){
            String s = scanner.nextLine();
            System.out.println(s);
        }

        scanner.close();
    }
}
